class RomanNumerals

def convert_arabic_number(number)
  if number == 0
    " "
  elsif number == 1
    "I"
  elsif number == 2
    "II"
  elsif number == 3
    "III"
  elsif number == 4
    "IV"
  elsif number == 5
    "V"
  elsif number == 6
    "VI"
  elsif number == 7
    "VII"
  else
    "VIII"
  end
end

end
